"""
shared/classifier.py — Классификатор типа документа АСУД (DOC_TYPE_MAP индекс)
по тексту .msg-письма.

УРОВЕНЬ 1 — точная реплика классификатора начальника (Power Query).
   154/154 совпадение на эталонном реестре Почта_ОЭК.xlsx.

УРОВЕНЬ 2 — расширения для случаев которые Уровень 1 оставляет в cat-0:
   • Госорганы (.gov.ru / УМВД / Росстат / Росгвардия / прокуратура) → 7
   • Юрики не-ООО (АНО / НКО) → 5
   • Физик-маркеры (л/с / перерасчёт / горячей воде / поверка счётчика) → 8

FALLBACK по домену отправителя — для писем БЕЗ ярких маркеров (одни
вложения + подпись): извлекаем 'From: Name <email@domain>' из тела
и:
   .gov.ru                                  → 7
   mail.ru/yandex/bk/list/inbox/etc         → 8
   любой другой корпоративный домен         → 5

Возвращает:
   8 / 5 / 7  — тип в DOC_TYPE_MAP
   0          — fallback не сработал (вызывающий код force'ит 8 + черновик)
  -1          — отбраковка («случайно отправил» — пропуск без создания)
"""

import re

# Бесплатные почтовые сервисы → физик-отправитель
_PERSONAL_DOMAINS = frozenset({
    'mail.ru', 'yandex.ru', 'gmail.com', 'bk.ru', 'list.ru',
    'inbox.ru', 'rambler.ru', 'internet.ru', 'mailop.ru',
    'icloud.com', 'ya.ru',
})

# Правила: (тип проверки, паттерн, тип_индекс). 'in' = substring, 're' = regex.
# Порядок важен — первое сработавшее правило выигрывает. Регистр учитывается
# (повторяем поведение Power Query Text.Contains).
_RULES = [
    # ====== УРОВЕНЬ 1: классификатор начальника ======
    ('in', 'квартир',                                8),
    ('in', 'кв.',                                    8),
    ('in', ' кв ',                                   8),
    ('in', 'сек.',                                   8),
    ('in', 'разделение лицевого счёта',              8),
    ('in', 'Электроэнергия',                         8),
    ('in', 'кабинет',                                8),
    ('in', 'Управление опеки и попечительства',      7),
    ('in', 'admomsk.omskportal.ru',                  7),
    ('in', 'mptk55.ru',                              5),
    ('in', 'ООО ',                                   5),
    ('in', 'omskportal.ru',                          5),
    ('in', 'КПП',                                    5),
    ('in', 'Помещение не жилое',                     5),
    ('in', 'офис',                                   5),
    ('in', 'квитанция по оплате теплоснабжения',     5),
    ('in', 'ТСЖ',                                    5),
    ('in', 'договор',                                5),
    ('in', 'письмо случайно отправил',              -1),
    # ====== УРОВЕНЬ 2: расширения ======
    # Госорганы → 7
    ('in', 'УМВД',                                   7),
    ('in', 'Росгвардия',                             7),
    ('in', 'Росстат',                                7),
    ('in', 'Прокуратура',                            7),
    ('in', 'прокуратур',                             7),
    ('in', '.gov.ru',                                7),
    # Юрики не-ООО → 5
    ('in', 'АНО ',                                   5),
    ('in', 'НКО ',                                   5),
    # Физик-маркеры → 8
    ('in', 'л/с',                                    8),
    ('in', 'лицевой счёт',                           8),
    ('in', 'лицевой счет',                           8),
    ('in', 'перерасчёт',                             8),
    ('in', 'перерасчет',                             8),
    ('in', 'горячей воде',                           8),
    ('in', 'поверка счётчик',                        8),
    ('in', 'поверки счётчик',                       8),
]


def _extract_sender_domain(body):
    """Извлекает домен отправителя из FW-шапки 'From: Name <email@domain>'.
    Берёт ПЕРВУЮ From-строку (актуальный пересыл). None если не нашли."""
    if not body:
        return None
    m = re.search(r'From:\s*[^<]*<([^>]+)>', body)
    if m:
        email = m.group(1).strip().lower()
        if '@' in email:
            return email.split('@')[-1]
    return None


def classify_doc_type(body):
    """Определяет тип документа АСУД (DOC_TYPE_MAP индекс) по тексту письма.

    Возвращает:
      8 / 5 / 7  — известный тип
      0          — ни одно правило и нет домена отправителя (полный fallback)
     -1          — помечено как 'случайно отправил' — вызывающий код пропускает
    """
    if not body:
        return 0
    # 1. Правила (Уровень 1 + Уровень 2)
    for kind, pat, t in _RULES:
        if kind == 'in':
            if pat in body:
                return t
        elif kind == 're':
            if re.search(pat, body):
                return t
    # 2. Fallback по домену отправителя
    domain = _extract_sender_domain(body)
    if domain:
        if 'gov.ru' in domain:
            return 7
        if domain in _PERSONAL_DOMAINS:
            return 8
        return 5  # любой другой корпоративный домен → юрик
    return 0
